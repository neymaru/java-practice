package com.programmers;

class Lv0_03_두수의곱 {
    public int solution(int num1, int num2) {
        return num1 * num2;
    }

    public static void main(String[] args) {
        Lv0_03_두수의곱 result = new Lv0_03_두수의곱();
        System.out.println(result.solution(3, 4));
        System.out.println(result.solution(27, 19));
    }
}



